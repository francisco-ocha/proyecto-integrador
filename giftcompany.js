window.onload = function()
{
  var category = ["Gastronomia", "entretenimiento", "Relax", "Viajes", "Cursos", "Aventura"];

  switch (category) {
    case "Gastronomia": element.style.color = "rgba(169, 229, 227)";
      break;
    case "Entretenimiento": element.style.color = "rgba(169, 229, 227)";
    break;
    case "Relax": element.style.color = "rgba(169, 229, 227)";
      break;
    case "Viajes": element.style.color = "rgba(169, 229, 227)";
    break;
    default: element.style.color = "rgba(169, 229, 227)";
  }
}
